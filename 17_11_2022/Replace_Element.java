package Lab_seasons;
import java.util.*;
/*    Problem Statement 5:
 Write a Java program to replace an element in a linked list.   */

public class Replace_Element {
			public static void main(String[] args) {
			LinkedList<String> all = new LinkedList<String>();
			all.add("Rose");
			all.add("Lavender");
			all.add("Lotas");
			all.add("Jasmine");
		    System.out.println("The List is: " + all);
		    all.set(2, "Sunflower");
		    System.out.println("The List is: " + all);
			}}
